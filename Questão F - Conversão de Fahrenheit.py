celsius = float(input("Digite a temperatura em Celsius: ")) # Recebe o valor da temperatura em graus Celsius que o usuario coloca
conversao = celsius * 1.8 + 32 # Converte o valor de celsius para fahrenheit, multiplicando o celsius por 1.8 e somando + 32

print("Valor convertido em Fahrenheit:", conversao) # Printa a conversão feita para fahrenheit

