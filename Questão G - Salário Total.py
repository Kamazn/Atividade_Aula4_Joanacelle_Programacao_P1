horas = float(input("Digite as horas trabalhadas: ")) # Recebe as horas trabalhadas que o usuario coloca
valor = float(input("Digite o valor da hora trabalhada: ")) # Recebe o valor de hora trabalhada que o usuario coloca
salario = horas * valor # Calcula o salário total multiplicando as horas trabalhadas pelo valor da hora

print("Salário Total:", salario)  # Printa o resultado do salário total 


